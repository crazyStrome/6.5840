// the service says it has created a snapshot that has
// all info up to and including index. this means the
// service no longer needs the log through (and including)
// that index. Raft should now trim its log as much as possible.
func (rf *Raft) Snapshot(index int, snapshot []byte) {
	// Your code here (2D).
	rf.mu.Lock()
	defer rf.mu.Unlock()
	var lastIncludedTerm int64
	entries := make([]Entry, 0, len(rf.log))
	for _, entry := range rf.log {
		if entry.Index < index {
			continue
		}
		if entry.Index == index {
			lastIncludedTerm = entry.Term
			continue
		}
		entries = append(entries, entry.clone())
	}
	rf.log = entries
	rf.snapshot = &Snapshot{
		LastIncludedIndex: index,
		LastIncludedTerm:  lastIncludedTerm,
		Snapshot:          clone(snapshot),
	}
}

// example code to send a RequestVote RPC to a server.
// server is the index of the target server in rf.peers[].
// expects RPC arguments in args.
// fills in *reply with RPC reply, so caller should
// pass &reply.
// the types of the args and reply passed to Call() must be
// the same as the types of the arguments declared in the
// handler function (including whether they are pointers).
//
// The labrpc package simulates a lossy network, in which servers
// may be unreachable, and in which requests and replies may be lost.
// Call() sends a request and waits for a reply. If a reply arrives
// within a timeout interval, Call() returns true; otherwise
// Call() returns false. Thus Call() may not return for a while.
// A false return can be caused by a dead server, a live server that
// can't be reached, a lost request, or a lost reply.
//
// Call() is guaranteed to return (perhaps after a delay) *except* if the
// handler function on the server side does not return.  Thus there
// is no need to implement your own timeouts around Call().
//
// look at the comments in ../labrpc/labrpc.go for more details.
//
// if you're having trouble getting RPC to work, check that you've
// capitalized all field names in structs passed over RPC, and
// that the caller passes the address of the reply struct with &, not
// the struct itself.
func (rf *Raft) sendRequestVote(server int, args *RequestVoteArgs, reply *RequestVoteReply) bool {
	ok := rf.peers[server].Call("Raft.RequestVote", args, reply)
	return ok
}

// the service using Raft (e.g. a k/v server) wants to start
// agreement on the next command to be appended to Raft's log. if this
// server isn't the leader, returns false. otherwise start the
// agreement and return immediately. there is no guarantee that this
// command will ever be committed to the Raft log, since the leader
// may fail or lose an election. even if the Raft instance has been killed,
// this function should return gracefully.
//
// the first return value is the index that the command will appear at
// if it's ever committed. the second return value is the current
// term. the third return value is true if this server believes it is
// the leader.
func (rf *Raft) Start(command interface{}) (int, int, bool) {
	// Your code here (2B).
	if rf.getCurrentState() != StateLeader {
		return -1, -1, false
	}
	curTerm := rf.getCurrentTerm()
	rf.mu.Lock()
	var lastIdx int
	if rf.snapshot != nil {
		lastIdx = rf.snapshot.LastIncludedIndex
	}
	if len(rf.log) != 0 {
		lastIdx = rf.log[len(rf.log)-1].Index
	}
	rf.log = append(rf.log, Entry{
		Index:   lastIdx + 1,
		Term:    curTerm,
		Command: command,
	})
	rf.mu.Unlock()
	rf.persist()
	rf.Logf("[Start] entries:%+v", rf.cloneLog())

	rf.nextIndex[rf.me] = lastIdx + 2
	rf.matchIndex[rf.me] = lastIdx + 1

	rf.Logf("[Start] append log:%+v at index:%v\n", command, lastIdx+1)

	return lastIdx + 1, int(curTerm), true
}

